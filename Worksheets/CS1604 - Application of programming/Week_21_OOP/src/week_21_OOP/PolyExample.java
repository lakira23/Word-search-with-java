package week_21_OOP;

public class PolyExample {
	public static int int_doubles(int one, int two) {
		return one * two;
	}
	
	public static int int_triples (int one, int two, int three) {
		return one * two * three;
	}
	
	public static double doubles_doubles(double one, double two) {
		return one * two;
	}
	
	public static double double_triples (double one, double two, double three) {
		return one * two * three;
	}
}
